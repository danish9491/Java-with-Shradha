import java.util.*;

public class product_ab {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter two numbers: ");
        int a = sc.nextInt();
        int b = sc.nextInt();
        System.out.print("Product of "+a+" and "+b+ " is: "+a*b);
    }
}