import java.util.*;

public class Q2 {
    public static void main(String[] args) {
                // Print numbers from 1 to n
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int counter = 1;
        while(counter<=n) {
            System.out.println(counter);
            counter++;
        }
    }
    
}
